void solve()
{
    int n, d, k;
    cin >> n >> d >> k;

    vpi v(k);
    for (int i = 0; i < k; i++)
    {
        cin >> v[i].first >> v[i].second;
    }

    vi pref(n + 1);
    vi suff(n + 1);

    vpi v2;
    for (auto i : v)
    {
        v2.pb({i.second, i.first});
    }

    sort(all(v));
    sort(all(v2));

    // cout << "v: ";
    // print(v);
    // cout << "v2: ";
    // print(v2);

    int pointer = 0;
    int current = 0;

    map<int, int> starting;
    map<int, int> ending;

    for (auto i : v)
    {
        starting[i.first]++;
        ending[i.second]++;
    }

    vi starting_v;
    vi ending_v;

    for (auto i : starting)
    {
        starting_v.pb(i.first);
    }
    for (auto i : ending)
    {
        ending_v.pb(i.first);
    }

    ll s_v_sz = starting_v.size();
    ll e_v_sz = ending_v.size();
    pref[0] = 0;
    for (int i = 1; i <= n; i++)
    {
        if (pointer < s_v_sz && i == starting_v[pointer])
        {
            pref[i] = starting[starting_v[pointer]] + pref[i - 1];
            pointer++;
        }
        else
        {
            pref[i] = pref[i - 1];
        }
    }
    // cout << "Pref: ";
    // print(pref);

    suff[0] = 0;
    pointer = 0;
    for (int i = 1; i <= n; i++)
    {
        if ((pointer < e_v_sz) &&
            ((i - 1) == (ending_v[pointer])))
        {
            suff[i] = ending[ending_v[pointer]] + suff[i - 1];
            pointer++;
        }
        else
        {
            suff[i] = suff[i - 1];
        }
    }
    // cout << "Suff: ";
    // print(suff);

    pair<int, int> bro = {-1, -1};
    pair<int, int> moth = {-1, INT_MAX};

    for (int i = 1; i <= n; i++)
    {
        // bro
        if (i + d - 1 > n)
        {
            break;
        }
        int overlapp = pref[min(n, i + d - 1)] - suff[i];
        // cout << "i: " << i << ", overlapp: " << overlapp << endl;
        if (overlapp > bro.second)
        {
            bro.second = overlapp;
            bro.first = i;
        }

        // mm
        if (overlapp < moth.second)
        {
            moth.second = overlapp;
            moth.first = i;
        }
    }

    cout << bro.first << " " << moth.first << endl;
}
