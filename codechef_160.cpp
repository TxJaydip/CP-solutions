bool isPali(string s)
{
    string r = s;
    reverse(all(s));
    if (s == r)
    {
        return 1;
    }
    return 0;
}

vector<std::vector<int>> generateVectors(int N, int M)
{
    std::vector<std::vector<int>> result;
    std::vector<int> current(N, 1); // Start with a vector of size N, all elements set to 1

    while (true)
    {
        result.push_back(current); // Store the current vector

        // Increment the vector in a way that counts in base M
        int index = N - 1;
        while (index >= 0 && current[index] == M)
        {
            current[index] = 1; // Reset current position to 1
            --index;            // Move to the next position on the left
        }

        if (index < 0)
            break; // If all positions are reset, we're done

        ++current[index]; // Increment the current position
    }

    return result;
}

vi calculateVecC(vi &b, int x)
{
    vi c;
    for (auto i : b)
    {
        c.pb(i % x);
    }
    return c;
}

vi goodXs(vi &v)
{
    vi ans;
    for (int i = 1; i <= 50; i++)
    {
        vi calculatedC = calculateVecC(v, i);
        vi revC = calculatedC;
        reverse(all(calculatedC));
        if (revC == calculatedC)
        {
            ans.pb(i);
        }
    }
    if (ans.size() == 50)
    {
        return {-1};
    }
    return ans;
}

