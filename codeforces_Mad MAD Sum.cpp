#include "bits/stdc++.h"
using namespace std;

#define ll long long unsigned int
#define pb push_back
#define vi vector<int64_t>

void iv(vi &v, int n)
{
    int temp;
    for (int i = 0; i < n; i++)
    {
        cin >> temp;
        v.pb(temp);
    }
}

void ov(vi &v)
{
    for (auto i : v)
    {
        cout << i << " ";
    }
    cout << endl;
}

void solve()
{
    int n;
    cin >> n;
    vi v;
    iv(v, n);
    // cout << "orgV: ";
    // ov(v);
    int64_t answer = accumulate(v.begin(), v.end(), 0ll);
    // cout << "Org Sum : " << answer << endl;
    map<int, int> count1;
    int maxi = 0;
    for (int i = 0; i < n; i++)
    {
        count1[v[i]]++;
        if (count1[v[i]] > 1 && v[i] > maxi)
        {
            maxi = v[i];
        }
        v[i] = maxi;
    }
    // cout << "op1V: ";
    // ov(v);
    answer = accumulate(v.begin(), v.end(), answer);
    // cout << "op1V Sum: " << answer << endl;
    count1.clear();
    // count1(n+1, 0);
    maxi = 0;
    for (int i = 0; i < n; i++)
    {
        count1[v[i]]++;
        if (count1[v[i]] > 1)
        {
            maxi = v[i];
        }
        v[i] = maxi;
    }
    // cout << "op2V: ";
    // ov(v);
    // answer = accumulate(v.begin(), v.end(), answer);
    // cout << "op2V Sum :" << answer << endl;
    count1.clear();
    map<int, int> mp;
    for (int i = 0; i < n; i++)
    {
        mp[v[i]]++;
    }

    for (auto [a, b] : mp)
    {
        // cout << "ele: " << a << ", freq: " << b;
        int temp = answer;
        if (b > 1)
        {
            answer +=
                ((n - (upper_bound(v.begin(), v.end(), a) - v.begin())) * b * a) +
                ((1ll * b * (1ll * b + 1)) * (1ll * a) / (1ll * 2));
        }
        else
        {
            answer += a;
        }
        // cout << ", added: " << answer - temp << endl;
    }
    cout << answer << endl;
}

int32_t main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}

