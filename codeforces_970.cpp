ll modExp(ll base, ll exp, ll mod)
{
    ll result = 1;
    base = base % mod;

    while (exp > 0)
    {
        if (exp % 2 == 1)
        {
            result = (result * base) % mod;
        }
        exp = exp >> 1;
        base = (base * base) % mod;
    }

    return result;
}

ll modDivide(ll P, ll Q, ll mod)
{
    ll gcd1 = __gcd(P, Q);
    P /= gcd1;
    Q /= gcd1;
    ll ans = modExp(Q, mod - 2, mod);
    return (P * ans) % mod;
}

void solve()
{
    ll n;
    cin >> n;
    vi v(n);
    iv(v);
    ll sum = 0;
    vector<ll> pref(n + 1, 0);
    for (int i = n - 1; i >= 0; i--)
    {
        pref[i] = (pref[i + 1] + v[i]) % MOD;
    }
    // ll sum = 0;
    for (int i = n - 2; i >= 0; i--)
    {
        sum += ((v[i] * (pref[i] - v[i])) % MOD);
        sum %= MOD;
    }
    ll num = (n * (n - 1) / 2) % MOD;
    if (sum % num == 0)
    {
        cout << (sum / num) % MOD << endl;
        return;
    }
    // cout << "Sum: " << sum << ", n: " << num << ", Ans: ";
    cout << modDivide(sum, num, MOD) % MOD << endl;
}

