ll modex1(ll a, ll b)
{
    if (b == 0)
    {
        return 1;
    }
    a %= MOD;
    ll temp = modex1(a, b / 2);
    temp *= temp;
    temp %= MOD;
    if (b % 2)
    {
        temp *= a;
        temp %= MOD;
    }
    return temp;
}

ll mod(ll a, ll b)
{
    return ((a % MOD) * modex1(b, MOD - 2)) % MOD;
}

