ll funK0(vi &v)
{
    ll mini = *min_element(all(v));
    ll maxi = *max_element(all(v));
    long double val = ((long double)(mini) + (long double)(maxi)) / (long double)(2);
    ll ans1 = LLONG_MAX, ans2 = LLONG_MAX;
    ll val1 = floorl(val);
    ll val2 = ceill(val);

    for (int i = 1; i < v.size(); i++)
    {
        ans1 = min(ans1, (abs(val1 - v[i])));
        ans2 = min(ans2, (abs(v[i] - val2)));
    }

    return min(ans1, ans2);
}

bool check(vi &v, ll k, ll maxDiff)
{
    ll n = v.size();
    ll totalSeg = 1ll;
    ll left = v[0] - maxDiff;
    ll right = v[0] + maxDiff;

    for (int i = 1; i < n; i++)
    {
        left = max(left, v[i] - maxDiff);
        right = min(right, v[i] + maxDiff);

        if (left > right)
        {
            totalSeg++;
            left = v[i] - maxDiff;
            right = v[i] + maxDiff;
        }
    }

    if (totalSeg <= (k + 1ll))
        return 1;
    return 0;
}

