class p
{
public:
    int x;
    int y;
};

int solve(int len, int b, p points[])
{
    vector<pair<int, int>> sPoints;

    cout << "Size of points: " << sizeof(points) / sizeof(points[0]) << endl;
    for (int i = 0; i < 9; i++)
    {
        sPoints.push_back({points[i].x, points[i].y});
    }

    // print(sPoints);
    // cout << "Len: " << len << ", breadth : " << b << endl;
    int answer = 0;

    for (int i = 0; i < 10; i++)
    {
        for (int j = 0; j < 10; j++)
        {
            int currentResult;
            {
                currentResult = 0;
                int xStart = i;
                int yStart = j;
                int xEnd = i + len;
                int yEnd = j + b;

                for (auto i : sPoints)
                {
                    int xx = i.first;
                    int yy = i.second;
                    if (xx <= xEnd && xx >= xStart && yy <= yEnd && yy >= yStart)
                    {
                        currentResult++;
                    }
                }
                answer = max(answer, currentResult);
            }
            {
                currentResult = 0;
                int xStart = i;
                int yStart = j;
                int xEnd = i + b;
                int yEnd = j + len;
                for (auto i : sPoints)
                {
                    int xx = i.first;
                    int yy = i.second;
                    if (xx <= xEnd && xx >= xStart && yy <= yEnd && yy >= yStart)
                    {
                        currentResult++;
                    }
                }
                answer = max(answer, currentResult);
            }
        }
    }

    return answer;
}

