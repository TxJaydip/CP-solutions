bool checkForK(vi v, ll k)
{
    if (v.size() % 2 == 0)
    {
        vi diffs;
        for (int i = 0; i < v.size(); i += 2)
        {
            diffs.pb(v[i + 1] - v[i]);
        }
        if (*max_element(all(diffs)) <= k)
        {
            return true;
        }
        else
        {
            return false;
        }
    }

    ll n = v.size();
    for (int i = 0; i < n; i += 2)
    {
        int to_skip = i;
        if (i == 0 && v[i] == 0ll && 1ll == v[i + 1])
        {
            continue;
        }
        if (i == n - 1 && v[i] == 1e18 && v[i - 1] == v[i] - 1ll)
        {
            continue;
        }
        if (i != 0 && i != n - 1 && v[i - 1] == v[i] - 1ll && v[i + 1] == v[i] + 1ll)
        {
            continue;
        }
        vi v2;
        for (int j = 0; j < n; j++)
        {
            if (j != to_skip)
            {
                v2.pb(v[j]);
            }
        }

        ll maxi = 0;
        for (int j = 0; j < v2.size(); j += 2)
        {
            maxi = max(maxi, v2[j + 1] - v2[j]);
        }
        if (maxi <= k)
        {
            // cout << "v and c2: " << endl;
            // print(v);
            // print(v2);
            return true;
        }
    }

    return false;
}

