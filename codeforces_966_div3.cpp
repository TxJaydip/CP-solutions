int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int t;
    cin >> t;
    while (t--)
    {
        int n, m, k;
        cin >> n >> m >> k;
        int w;
        cin >> w;
        vi v;
        iv(v, w);
        sort(v.rbegin(), v.rend());

        vector<vector<int>> grid(n, vector<int>(m, 0));
        // cout << "rows: " << grid.size() << ", columns: " << grid[0].size() << endl;
        for (int i = 0; i < min(max(0, m - k + 1), k); i++)
        {
            grid[0][i] = i + 1;
            grid[0][m - i - 1] = i + 1;
        }
        int index = 0;
        while (index < m && grid[0][index] != 0)
        {
            index++;
        }
        while (index < m && grid[0][index] == 0)
        {
            grid[0][index] = grid[0][index - 1];
            index++;
        }

        for (int i = 0; i < m; i++)
        {
            grid[n - 1][i] = grid[0][i];
        }

        for (int j = 1; j < min(max(0, n - k + 1), k); j++)
        {
            for (int i = 0; i < m; i++)
            {
                grid[j][i] = grid[0][i] * (j + 1);
                grid[n - j - 1][i] = grid[0][i] * (j + 1);
            }
        }

        index = 0;
        while (index < n && grid[index][0] != 0)
        {
            index++;
        }
        while (index < n && grid[index][0] == 0)
        {
            for (int i = 0; i < m; i++)
            {
                grid[index][i] = grid[index - 1][i];
            }

            index++;
        }

        map<ll, ll> mp;
        for (auto row : grid)
        {
            for (auto ele : row)
            {
                mp[ele]++;
            }
        }

        vector<pair<ll, ll>> cnt;
        for (auto i : mp)
        {
            cnt.pb(i);
        }
        // sort(cnt.begin(), cnt.end(), cmp);
        reverse(cnt.begin(), cnt.end());

        index = 0;
        ll answer = 0;
        for (auto i : v)
        {
            answer += (i * cnt[index].first);
            cnt[index].second--;
            if (cnt[index].second == 0)
            {
                index++;
            }
        }
        cout << answer << endl;

        // print(cnt);
        // print(mp);

        // print(grid);
    }
    return 0;
}

