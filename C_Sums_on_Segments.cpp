ll findLow(ll stInd, ll dir, vi &v)
{
    if (dir == 1)
    {
        ll low = v[stInd];
        for (ll i = stInd; i < v.size(); i++)
        {
            low = min(low, v[i]);
        }
        return low;
    }
    else
    {
        ll low = v[stInd];
        for (ll i = stInd; i >= 0; i--)
        {
            low = min(low, v[i]);
        }
        return low;
    }
}

ll findHigh(ll stInd, ll dir, vi &v)
{
    if (dir == 1)
    {
        ll high = v[stInd];
        for (ll i = stInd; i < v.size(); i++)
        {
            high = max(high, v[i]);
        }
        return high;
    }
    else
    {
        ll high = v[stInd];
        for (ll i = stInd; i >= 0; i--)
        {
            high = max(high, v[i]);
        }
        return high;
    }
}

