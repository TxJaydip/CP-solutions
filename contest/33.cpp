#include "bits/stdc++.h"
using namespace std;

#define ll long long int
#define pb push_back
#define vi vector<long long int>

void iv(vi &v, int n)
{
	ll temp;
	for (int i = 0; i < n; i++)
	{
		cin >> temp;
		v.pb(temp);
	}
}

void ov(vector<long long int> &v)
{
	for (auto i : v)
	{
		cout << i << " ";
	}
	cout << endl;
}

int32_t main()
{
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	int t;
	cin >> t;
	while (t--)
	{
		int n;
		cin >> n;
		vector<long long int> v;
		iv(v, n);
		vector<long long int> op(n, 0);
		vector<long long int> pref(n, 0);
		int i1 = 0;
		while (i1 < n && v[i1] == 1)
		{
			i1++;
		}
		if (i1 == n)
		{
			cout << 0 << endl;
			continue;
		}
		int flag = 0;
		long long int temp;
		for (int i = max(i1, 1); i < n; i++)
		{
			if (v[i] == 1)
			{
				flag = 1;
				break;
			}

			if (v[i] == v[i - 1])
			{
				pref[i] = pref[i - 1];
				continue;
			}
			if (v[i] < v[i - 1])
			{
				temp = ceil(log(v[i - 1]) / log(v[i]));
				temp = ceill(log(temp) / log(2ll));
				pref[i] = pref[i - 1] + temp;
			}
			else
			{
				swap(v[i], v[i - 1]);
				temp = floor(log(v[i - 1]) / log(v[i]));
				temp = floor(log(temp) / log(2ll));
				pref[i] = max(0ll, (pref[i - 1] + (-1) * temp));
				swap(v[i], v[i - 1]);
			}
		}
		// cout << "==============\n";
		// ov(pref);
		if (flag)
		{
			cout << -1 << endl;
			continue;
		}
		cout << accumulate(pref.begin(), pref.end(), 0ll) << endl;
	}
	return 0;
}

