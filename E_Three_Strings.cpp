ll diff(string a, string b)
{
    ll ans = 0;
    for (int i = 0; i < a.length(); i++)
    {
        if (a[i] != b[i])
        {
            ans++;
        }
    }
    return ans;
}

vector<string> allStrings(string a, string b)
{
    vector<string> result;
    function<void(string, int, int)> dfs = [&](string current, int i, int j)
    {
        if (i == a.size() && j == b.size())
        {
            result.push_back(current);
            return;
        }
        if (i < a.size())
            dfs(current + a[i], i + 1, j);
        if (j < b.size())
            dfs(current + b[j], i, j + 1);
    };
    dfs("", 0, 0);
    return result;
}

